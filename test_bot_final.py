async def handle_answer(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    user_id = update.effective_user.id
    callback = query.data
    
    if callback == "menu_start":
        return await begin_test(update, context, user_id)
    elif callback == "menu_last_result":
        return await show_last_result(update, context, user_id)
    elif callback == "menu_help":
        return await show_menu_help(update, context)
    elif callback == "menu_back":
        return await show_main_menu(update, context, user_id)
    elif callback == "menu_cancel":
        return await cancel_test(update, context, user_id)
    elif callback == "show_errors":
        return await show_errors(update, context, user_id)
    
    if user_id not in user_data:
        await query.edit_message_text(
            "❌ Нет активного теста.",
            reply_markup=get_main_menu_keyboard(),
            parse_mode='Markdown'
        )
        return ASKING
    
    data = user_data[user_id]
    current = data["current"]
    if current >= len(data["questions"]):
        return await show_results(update, context, user_id)
    
    question = data["questions"][current]
    is_multi = len(question["correct"]) > 1
    
    if callback.endswith("_done"):
        data["answers"][current] = list(data.get("selected_for_current", set()))
        data["current"] += 1
        data["selected_for_current"] = set()
        return await show_question(update, context, user_id)
    
    parts = callback.split("_")
    q_index = int(parts[0][1:])
    a_index = int(parts[1][1:])
    
    if is_multi:
        selected = data.get("selected_for_current", set())
        if a_index in selected:
            selected.remove(a_index)
        else:
            selected.add(a_index)
        data["selected_for_current"] = selected
        text = format_question_text(question, current + 1, len(data["questions"]), selected)
        keyboard = get_options_keyboard(question, current, selected)
        await query.edit_message_text(text=text, reply_markup=keyboard, parse_mode='Markdown')
        return ASKING
    else:
        data["answers"][current] = [a_index]
        data["current"] += 1
        data["selected_for_current"] = set()
        return await show_question(update, context, user_id)

async def show_results(update: Update, context: ContextTypes.DEFAULT_TYPE, user_id: int):
    data = user_data[user_id]
    questions = data["questions"]
    answers = data["answers"]
    
    correct_count = 0
    wrong_answers = []
    
    for i, question in enumerate(questions):
        selected = set(answers.get(i, []))
        correct = set(question["correct"])
        if selected == correct:
            correct_count += 1
        else:
            wrong_answers.append({
                "question": question["question"],
                "your_answer": [question["options"][j] for j in sorted(selected)] if selected else ["Не отвечено"],
                "correct_answer": [question["options"][j] for j in question["correct"]]
            })
    
    score = correct_count
    percentage = round((score / 30) * 100, 2)
    last_results[user_id] = {
        'correct_count': correct_count,
        'score': score,
        'percentage': percentage,
        'wrong_answers': wrong_answers
    }
    
    result_text = (
        "📊 *РЕЗУЛЬТАТЫ ТЕСТИРОВАНИЯ*\n\n"
        "✅ Правильных ответов: " + str(correct_count) + " из 30\n"
        "📈 Баллов: " + str(score) + " / 30.00\n"
        "🎯 Оценка: " + str(percentage) + "%\n\n"
    )
    if percentage >= 90:
        result_text += "🏆 *Отличный результат!*\n\n"
    elif percentage >= 70:
        result_text += "👍 *Хороший результат!*\n\n"
    elif percentage >= 50:
        result_text += "📚 *Удовлетворительно, есть куда расти*\n\n"
    else:
        result_text += "💪 *Стоит повторить материал*\n\n"
    
    if not wrong_answers:
        result_text += "🎉 *Идеально! Все ответы правильные!*"
    
    keyboard = [
        [InlineKeyboardButton("📝 Новый тест", callback_data="menu_start")],
        [InlineKeyboardButton("📊 Последний результат", callback_data="menu_last_result")],
        [InlineKeyboardButton("❌ Показать ошибки", callback_data="show_errors")],
        [InlineKeyboardButton("👋 Главное меню", callback_data="menu_back")]
    ]
    
    if update.callback_query:
        await update.callback_query.edit_message_text(
            text=result_text,
            reply_markup=InlineKeyboardMarkup(keyboard),
            parse_mode='Markdown'
        )
    else:
        await update.message.reply_text(
            text=result_text,
            reply_markup=InlineKeyboardMarkup(keyboard),
            parse_mode='Markdown'
        )
    
    if user_id in user_data:
        del user_data[user_id]
    return ASKING

