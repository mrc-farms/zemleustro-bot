def main():
    application = Application.builder().token(TOKEN).build()
    
    conv_handler = ConversationHandler(
        entry_points=[CommandHandler("start", start)],
        states={
            ASKING: [
                CallbackQueryHandler(handle_answer, pattern=r"^q\d+_(a\d+|done)$"),
                CallbackQueryHandler(handle_answer, pattern=r"^(menu_|show_errors)"),
                CommandHandler("cancel", cancel)
            ]
        },
        fallbacks=[CommandHandler("cancel", cancel)],
    )
    
    application.add_handler(conv_handler)
    application.add_handler(CommandHandler("help", help_command))
    
    # Проверяем, запущены ли мы на сервере (Render) или на компьютере
    if os.environ.get("RENDER"):
        PORT = int(os.environ.get("PORT", "10000"))
        RENDER_HOST = os.environ.get("RENDER_EXTERNAL_HOSTNAME")
        if RENDER_HOST:
            WEBHOOK_URL = f"https://{RENDER_HOST}/webhook"
            print("🌐 Запуск на сервере Render (webhook)...")
            application.run_webhook(
                listen="0.0.0.0",
                port=PORT,
                webhook_url=WEBHOOK_URL,
                secret_token=TOKEN.split(":")[1][:16]
            )
        else:
            print("❌ Не удалось определить RENDER_EXTERNAL_HOSTNAME")
    else:
        print("🤖 Запуск на компьютере (polling)...")
        application.run_polling()

if __name__ == "__main__":
    main()