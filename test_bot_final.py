def get_options_keyboard(question_data, question_index, selected=None):
    keyboard = []
    is_multi = len(question_data["correct"]) > 1
    
    for i, option in enumerate(question_data["options"]):
        btn_text = str(i + 1)
        if selected and i in selected:
            btn_text = "✅ " + btn_text
        keyboard.append([
            InlineKeyboardButton(
                btn_text, 
                callback_data="q" + str(question_index) + "_a" + str(i)
            )
        ])
    
    if is_multi:
        done_text = "✅ Готово"
        if selected:
            done_text = "✅ Готово (" + str(len(selected)) + " выбрано)"
        keyboard.append([InlineKeyboardButton(done_text, callback_data="q" + str(question_index) + "_done")])
    
    return InlineKeyboardMarkup(keyboard)

def format_question_text(question_data, q_num, total, selected=None):
    is_multi = len(question_data["correct"]) > 1
    
    text = "❓ *Вопрос " + str(q_num) + " из " + str(total) + "*"
    if is_multi:
        text += " *(выберите несколько вариантов)*"
    text += "\n\n" + question_data['question'] + "\n\n"
    
    for i, option in enumerate(question_data["options"]):
        marker = ""
        if selected and i in selected:
            marker = "✅ "
        text += str(i + 1) + ". " + marker + option + "\n"
    
    if is_multi and selected:
        text += "\n_Выбрано: " + str(len(selected)) + " из " + str(len(question_data['options'])) + "_"
    
    return text

